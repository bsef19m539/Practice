package com.example.learningapp;

public class Alphabet {
    public String alpha;
    public int imgid;

    public Alphabet(String alpha, int imgid) {
        this.alpha = alpha;
        this.imgid = imgid;
    }

    public String getAlpha() {
        return alpha;
    }

    public void setAlpha(String alpha) {
        this.alpha = alpha;
    }

    public int getImgid() {
        return imgid;
    }

    public void setImgid(int imgid) {
        this.imgid = imgid;
    }
}
